package test03;
//산술연산(Arithmetic) <=> 논리 연산(Logical)
public class Operation3 {
	public static void main(String[] args) {
		//+, -, *, /, % (*파이썬은 '**' >> 제곱)
		int su1 = 98;
		int su2 = 18;
		System.out.println("su1+su2= "+(su1+su2));
		System.out.println("su1-su2= "+(su1-su2));
		System.out.println("su1*su2= "+(su1*su2));
		System.out.println("su1/su2= "+(su1/su2));
		System.out.println("su1%su2= "+(su1%su2));		//*나누기 하고 남은 나머지 수
		
	}

}
